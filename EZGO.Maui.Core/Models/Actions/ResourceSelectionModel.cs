namespace EZGO.Maui.Core.Models.Actions
{
    /// <summary>
    /// Resource selection model.
    /// </summary>
    public class ResourceSelectionModel
    {
        /// <summary>
        /// Gets or sets the text.
        /// </summary>
        /// <value>
        /// The fullname.
        /// </value>
        public string Text { get; set; }

        /// <summary>
        /// Gets or sets a value indicating whether an icon should be shown.
        /// </summary>
        /// <value>
        ///   <c>true</c> if an icon should be shown; otherwise, <c>false</c>.
        /// </value>
        public bool ShowIcon { get; set; }
    }
}
